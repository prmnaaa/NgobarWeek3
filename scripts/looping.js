/**
 * Looping
 */

//!============== FOR ==============
for (let i = 0; i < 10; i++) {
  console.log(i);
}

//!============== WHILE ==============
let i = 0;
while (i < 10) {
  console.log(i);
  i++;
}

//!============== DO WHILE ==============
let j = 0;
do {
  console.log(j);
  j++;
} while (j < 10);